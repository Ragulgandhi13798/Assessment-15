package castingeg;

public class Inttofloat {

	public static void main(String[] args) {
		
		int a = 4145;
		float y = a;
		
		{
			System.out.println(y);
		}
		
		
		//float to int
		
		float f = (float) 45.3;
		int i = (int) f;
		{
			System.out.println(i);
		}
		

		//double to int
		
		double d = 343.56;
		int i1 = (int) d;
		
		System.out.println(i1);
		
		//short to float
		
		short s = 34;
		float f1 = s;
		
		System.out.println(f1);
		
		
		
	}

}
